def test_scalar_validation_rejects_dtype_and_shape(monkeypatch):
    fake = SimpleNamespace(
        device=torch.device("cuda", 0),
        numel=lambda: 2,
        dtype=torch.float32,
        is_contiguous=lambda: True,
        shape=(2,),
    )
    monkeypatch.setattr(torch, "Tensor", type(fake))
    with pytest.raises(ValueError, match="one contiguous"):
        _require_cuda_scalar("counter", fake, dtype=torch.long)


