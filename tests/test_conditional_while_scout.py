def test_conditional_wrapper_holds_children_and_destroys_once(monkeypatch):
    calls = []

    class Extension:
        @staticmethod
        def build_while(*args):
            calls.append(("build", args))
            return 77

        @staticmethod
        def launch(handle):
            calls.append(("launch", handle))

        @staticmethod
        def destroy(handle):
            calls.append(("destroy", handle))

    model = SimpleNamespace(raw_cuda_graph=lambda: 11)
    tail = SimpleNamespace(raw_cuda_graph=lambda: 22)
    tensors = [object(), object(), object()]

    monkeypatch.setattr(
        conditional_while_scout,
        "_require_cuda_scalar",
        lambda *args, **kwargs: torch.device("cuda", 2),
    )
    monkeypatch.setattr(conditional_while_scout, "_load_extension", Extension)

    guards = []

    @contextmanager
    def device_guard(device):
        guards.append(torch.device(device))
        yield

    monkeypatch.setattr(torch.cuda, "device", device_guard)
    graph = ConditionalGraph.while_children(
        model,
        tail,
        unfinished=tensors[0],
        physical_step=tensors[1],
        stop_step=tensors[2],
    )
    graph.replay()
    graph.close()
    graph.close()

    assert graph.source_graphs == (model, tail)
    assert calls[0][0] == "build"
    assert calls[0][1][:2] == (11, 22)
    assert calls.count(("launch", 77)) == 1
    assert calls.count(("destroy", 77)) == 1
    assert guards == [torch.device("cuda", 2)] * 3
    with pytest.raises(RuntimeError, match="closed"):
        graph.replay()


def test_counter_probe_reports_runtime_and_driver_versions(monkeypatch):
    class Extension:
        @staticmethod
        def build_probe(*args):
            return 12

        @staticmethod
        def runtime_version():
            return 12080

        @staticmethod
        def driver_version():
            return 13020

        @staticmethod
        def destroy(handle):
            pass

    monkeypatch.setattr(
        conditional_while_scout,
        "_require_cuda_scalar",
        lambda *args, **kwargs: torch.device("cuda", 0),
    )
    monkeypatch.setattr(conditional_while_scout, "_load_extension", Extension)

    @contextmanager
    def device_guard(device):
        yield

    monkeypatch.setattr(torch.cuda, "device", device_guard)
    graph = ConditionalGraph.counter_probe(object(), object())
    assert graph.versions() == {"runtime": 12080, "driver": 13020}
    graph.close()


